BUFF_ALERTS = {}
BUFF_ALERTS["Tricks of the Trade"] = "\124cFFFF0000TRICKS: CONSIDER NEW CORRUPTION"
BUFF_ALERTS["Storm Cloud"] = "\124cFFFF0000STORM: SPREAD THE BUFF AROUND"
BUFF_ALERTS["Storm Power"] = "\124cFFFF0000POWER: RECAST CORRUPTION AND PUMP"


local EventFrame = CreateFrame("frame", "EventFrame")
EventFrame:RegisterEvent("UNIT_AURA")

EventFrame:SetScript("OnEvent", function(self, event, ...)
	local target = ...;

	if target == "player" then

		for i=1,40 do
			current_buff_name = UnitAura("player", i)

			for buff,alert in pairs(BUFF_ALERTS) do
				if buff == current_buff_name then
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print(alert)
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
					print("x0x0x0x0x0x0x0x0x0x0x0x0")
				end
			end
		end
	end
end)
